a=lambda b:b+1
for i in range(1,6):
    print(a(i))
#In this example, a(5) calls the lambda function with the argument 5, and it returns 5 + 1, which is 6.
#The result is then printed to the console. Lambdas are often used for short,
#simple functions like this one, where you don't want to define a full named function.
